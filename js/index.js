function myFunction() {
  var x = document.getElementById("nav");
  if (x.style.display === "block") {
    x.style.display += " responsive";
  } else {
    x.style.display = "block";
  }
}

function x() {
  var x = document.getElementById("foot");
  if (x.style.display === "block") {
    x.style.display = "none";
  } else {
    x.style.display = "block";
  }
}